package Logical_Program;

public class Widening_Narrowing {
	

	public static void main(String[] args) {
		
		//widening
		int  a=2;
		int b=7;
		
		float res=a%b;
		
		System.out.println("res-"+res);
		
		//narrowing
		float x=2.012f;
		float y=3.12f;
		
		int sum=(int)(x+y);
		
		System.out.println("sum-"+sum);
		
		
		
		
	}

}
